/**
 * _puts - Prints a string followed by a new 
 * line to stdout.
 * @str: Pointer to the string to be printed.
 * This function prints the characters
 * of the input string to the standard output
 * followed by a new line character using the
 * standard library function puts.
 */
void _puts(char *str)
{
_putchar(str);
}
